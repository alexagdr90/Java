public class OperadoresAritmeticos {
    public static void main(String[] args) {
      
        int num=10;
        System.out.println("El numero  es "+num);

        num+=5;
        System.out.println("El numero ahora es "+num);
        
        num/=2;
        System.out.println("Y ahora es "+num);
        
        num++;
        System.out.println("Con num++ es "+num);

    }
}
